$(document).keyup(function (e) {
    if (e.keyCode == 37) {
        alert('Клавиша влево!');
    } else if (e.keyCode == 38) {
        alert('Клавиша вверх!');
    } else if (e.keyCode == 39) {
        alert('Клавиша вправо!');
    } else if (e.keyCode == 40) {
        alert('Клавиша вниз!');
    }
});